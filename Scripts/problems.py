def expand_9d_to_11d_scalar(x9):
    """将 9维 设计变量扩展为 11维 物理变量"""
    x9_arr = np.asarray(x9)
    return np.concatenate([x9_arr, np.array([0.0, 0.0])])

# ==============================================================================
#                 案例 1: Math 2D
# ==============================================================================

def math_2d_obj(x):
    return x[0] + x[1]

def math_2d_con(X):
    x1, x2 = X[:, 0], X[:, 1]
    ceq = np.zeros((X.shape[0], 3))
    ceq[:, 0] = x1**2 * x2 / 20 - 1
    ceq[:, 1] = (x1 + x2 - 5)**2 / 30 + (x1 - x2 - 12)**2 / 120 - 1
    ceq[:, 2] = 80 / (x1**2 + 8 * x2 - 5) - 1
    return ceq 

